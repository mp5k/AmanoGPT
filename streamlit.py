def init_messages():
    clear_button = st.sidebar.button("ä¼šè©±å±¥æ­´ã‚’ã‚¯ãƒªã‚¢", key="clear")
    if clear_button or "messages" not in st.session_state:
        st.session_state.messages = [
            SystemMessage(content="You are a helpful assistant.")
        ]
        st.session_state.costs = []


def select_model():
    model = st.sidebar.radio("ãƒ¢ãƒ‡ãƒ«", ("GPT-3.5", "GPT-4", "GPT-4o"))
    if model == "GPT-3.5":
        model_name = "gpt-3.5-turbo"
    elif model == "GPT-4":
        model_name = "gpt-4-turbo"
    elif model == "GPT-4o":
        model_name = "gpt-4o"

    # ã‚¹ãƒ©ã‚¤ãƒ€ãƒ¼ã‚’è¿½åŠ ã—ã€temperatureã‚’0ã‹ã‚‰2ã¾ã§ã®ç¯„å›²ã§é¸æŠå¯èƒ½ã«ã™ã‚‹
    # åˆæœŸå€¤ã¯0.0ã€åˆ»ã¿å¹…ã¯0.01ã¨ã™ã‚‹
    temperature = st.sidebar.slider("ãƒ©ãƒ³ãƒ€ãƒ æ€§ï¼ˆTemperatureï¼‰", min_value=0.0, max_value=2.0, value=0.0, step=0.01)

    return ChatOpenAI(temperature=temperature, model_name=model_name)


def get_answer(llm, messages):
    with get_openai_callback() as cb:
        answer = llm(messages)
    return answer.content, cb.total_cost


def main():
    api_key = os.getenv('OPENAI_API_KEY')
    print(api_key)
    if not api_key:
        st.error("OpenAI APIã‚­ãƒ¼ãŒè¨­å®šã•ã‚Œã¦ã„ã¾ã›ã‚“ã€‚ç’°å¢ƒå¤‰æ•° 'OPENAI_API_KEY' ã‚’è¨­å®šã—ã¦ãã ã•ã„ã€‚")
